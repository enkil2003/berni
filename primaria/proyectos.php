                                        <h2 class="margin-bottom">Feria de las cooperativas - 6to grado</h2>
                                        <p>El concepto de cooperativismo nos acerca la posibilidad de poder trabajar
con los niños nociones básicas de fundamentos sociales como la
igualdad, la equidad, el reparto, el trabajo en equipos entre otros.
Este proyecto estará gestionado por el Taller de Matemática por Deporte,
el Área de Matemática y el Equipo Directivo. Cada sector tiene
actividades y responsabilidades, fundamentalmente para el acompañamiento
a los alumnos. En tanto los niños deberán cumplir con actividades
que están dirigidas por los adultos.
La idea básica de este proyecto es el trabajo en equipos, los cuales deberán
recorrer varios trayectos para elaborar productos, donde la confección
de los mismos tiene un sustento conceptual y otro procedimental.
El equipo se basa en las igualitarias condiciones de trabajo y de
aportes, para concluir en la venta de lo producido.</p>
